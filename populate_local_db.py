Session = sessionmaker(bind=engine)
session = Session()

try:
    # Clear existing demo students
    deleted = session.execute(text("DELETE FROM students")).rowcount
    session.commit()
    if deleted:
        print(f"🗑️  Cleared {deleted} existing students")

    # Insert real students from backup
    for (sid, fname, lname, cls, cls_num, student_id, phone, email, school_id) in STUDENTS:
        school_name = SCHOOL_ID_TO_NAME.get(school_id, 'Unknown')
        session.execute(
            text("""
                INSERT INTO students (id, fname, lname, studentId, class, classNumber, parentPhone, email, school_name)
                VALUES (:id, :fname, :lname, :studentId, :class, :classNumber, :phone, :email, :school_name)
            """),
            {
                "id": sid,
                "fname": fname,
                "lname": lname,
                "studentId": student_id,
                "class": cls,
                "classNumber": cls_num,
                "phone": phone,
                "email": email,
                "school_name": school_name,
            }
        )

    session.commit()
    print(f"✅ Imported {len(STUDENTS)} students from backup into local SQLite.")

    # Summary by school
    rows = session.execute(text("SELECT school_name, COUNT(*) FROM students GROUP BY school_name ORDER BY school_name")).fetchall()
    print("\n📊 Students per school:")
    for school, count in rows:
        print(f"   {school}: {count}")

except Exception as e:
    print(f"❌ Error: {e}")
    session.rollback()
    raise
finally:
    session.close()
